import re
import sys

addx_re = re.compile(r'addx (0|-?[1-9]\d*)')


def execute(input):
    x = 1
    for line in input.splitlines():
        if line == 'noop':
            yield x
        elif m := addx_re.fullmatch(line):
            yield x
            yield x
            x += int(m[1])
        else:
            raise ValueError(f'invalid instruction {line!r}')


def part1(input):
    return sum(
        cycle * x
        for cycle, x in enumerate(execute(input), start=1)
        if cycle % 40 == 20
    )


